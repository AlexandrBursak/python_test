class MyCalendar:

    def __init__(self):
        self.booking = []

    def _reserv(self, start: int, end: int) -> None:
        self.booking.append([start, end])
    
    def _check(self, start: int, end: int) -> bool:
        for i in self.booking:
            if end <= i[0] or start >= i[1]:
                continue
            else:
                return False
        return True

    def book(self, start: int, end: int) -> bool:
        if self._check(start, end):
            self._reserv(start, end)
            return True
        return False


